// Name of package to be created
package MathPack;
 
// Class in which the above created package belong to
public class MathDemo {
    public void add(int n1, int n2) {
      System.out.println(n1+n2);
    }
    public void sub(int n1, int n2) {
      System.out.println(n1-n2);
    }
}


